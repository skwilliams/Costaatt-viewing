export const getCourses = function (courseList) {
  const progCourses = [];
  courseList.forEach((courseCode) => {
    progCourses.push(
      allCourses.find(
        (course) => course.coursecode.trim() === courseCode.trim()
      )
    );
  });
  return progCourses;
};

